create or replace function public.admin_guardar_inscripcion_jugador(
  p_inscripcion_id bigint default null,
  p_jugador_id bigint default null,
  p_nombre_completo text default null,
  p_aliases text[] default '{}',
  p_club_id bigint default null,
  p_torneo_id bigint default null,
  p_posicion text default 'sin_definir',
  p_dorsal smallint default null,
  p_estado text default 'por_verificar',
  p_fecha_desde date default null,
  p_fecha_hasta date default null,
  p_fuente text default null,
  p_observaciones text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_jugador public.jugadores;
  v_inscripcion public.inscripciones_jugadores;
begin
  if p_club_id is null or p_torneo_id is null then
    raise exception 'Club y torneo son obligatorios.';
  end if;

  if p_nombre_completo is null or btrim(p_nombre_completo) = '' then
    raise exception 'El nombre del jugador es obligatorio.';
  end if;

  if p_posicion not in (
    'arquero',
    'defensor',
    'mediocampista',
    'delantero',
    'sin_definir'
  ) then
    raise exception 'Posicion invalida.';
  end if;

  if p_estado not in ('confirmado', 'por_verificar', 'inactivo') then
    raise exception 'Estado invalido.';
  end if;

  if
    p_estado = 'confirmado'
    and (p_fuente is null or btrim(p_fuente) = '')
  then
    raise exception 'Un jugador confirmado debe tener una fuente.';
  end if;

  if p_dorsal is not null and (p_dorsal < 1 or p_dorsal > 99) then
    raise exception 'Dorsal invalido.';
  end if;

  if
    p_fecha_desde is not null
    and p_fecha_hasta is not null
    and p_fecha_hasta < p_fecha_desde
  then
    raise exception 'La fecha hasta no puede ser anterior a fecha desde.';
  end if;

  if p_inscripcion_id is not null then
    select *
    into v_inscripcion
    from public.inscripciones_jugadores
    where id = p_inscripcion_id
    for update;

    if not found then
      raise exception 'Inscripcion no encontrada.';
    end if;

    p_jugador_id := v_inscripcion.jugador_id;
  end if;

  if p_jugador_id is null then
    insert into public.jugadores (
      nombre_completo,
      aliases
    )
    values (
      btrim(p_nombre_completo),
      coalesce(p_aliases, '{}')
    )
    returning * into v_jugador;
  else
    update public.jugadores
    set
      nombre_completo = btrim(p_nombre_completo),
      aliases = coalesce(p_aliases, '{}'),
      actualizado_en = now()
    where id = p_jugador_id
    returning * into v_jugador;

    if not found then
      raise exception 'Jugador no encontrado.';
    end if;
  end if;

  if p_inscripcion_id is null then
    insert into public.inscripciones_jugadores (
      jugador_id,
      club_id,
      torneo_id,
      posicion,
      dorsal,
      estado,
      fecha_desde,
      fecha_hasta,
      fuente,
      observaciones
    )
    values (
      v_jugador.id,
      p_club_id,
      p_torneo_id,
      p_posicion,
      p_dorsal,
      p_estado,
      p_fecha_desde,
      p_fecha_hasta,
      nullif(btrim(p_fuente), ''),
      nullif(btrim(p_observaciones), '')
    )
    returning * into v_inscripcion;
  else
    update public.inscripciones_jugadores
    set
      club_id = p_club_id,
      torneo_id = p_torneo_id,
      posicion = p_posicion,
      dorsal = p_dorsal,
      estado = p_estado,
      fecha_desde = p_fecha_desde,
      fecha_hasta = p_fecha_hasta,
      fuente = nullif(btrim(p_fuente), ''),
      observaciones = nullif(btrim(p_observaciones), ''),
      actualizado_en = now()
    where id = p_inscripcion_id
    returning * into v_inscripcion;
  end if;

  return jsonb_build_object(
    'jugador', to_jsonb(v_jugador),
    'inscripcion', to_jsonb(v_inscripcion)
  );
end;
$$;

