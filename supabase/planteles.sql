create table if not exists public.torneos (
  id bigint generated by default as identity primary key,
  anio smallint not null check (anio between 2000 and 2100),
  tipo text not null check (tipo in ('apertura', 'clausura')),
  nombre text not null,
  activo boolean not null default false,
  fecha_inicio date null,
  fecha_fin date null,
  creado_en timestamptz not null default now(),
  actualizado_en timestamptz not null default now(),
  unique (anio, tipo)
);

insert into public.torneos (anio, tipo, nombre, activo)
values
  (2026, 'apertura', 'Apertura 2026', true),
  (2026, 'clausura', 'Clausura 2026', false)
on conflict (anio, tipo) do update
set nombre = excluded.nombre;

create table if not exists public.jugadores (
  id bigint generated by default as identity primary key,
  nombre_completo text not null,
  aliases text[] not null default '{}',
  activo boolean not null default true,
  creado_en timestamptz not null default now(),
  actualizado_en timestamptz not null default now()
);

create table if not exists public.inscripciones_jugadores (
  id bigint generated by default as identity primary key,
  jugador_id bigint not null references public.jugadores(id),
  club_id bigint not null references public.clubes(id),
  torneo_id bigint not null references public.torneos(id),
  posicion text not null default 'sin_definir' check (
    posicion in (
      'arquero',
      'defensor',
      'mediocampista',
      'delantero',
      'sin_definir'
    )
  ),
  dorsal smallint null check (dorsal between 1 and 99),
  estado text not null default 'por_verificar' check (
    estado in ('confirmado', 'por_verificar', 'inactivo')
  ),
  fecha_desde date null,
  fecha_hasta date null,
  fuente text null,
  observaciones text null,
  creado_en timestamptz not null default now(),
  actualizado_en timestamptz not null default now(),
  unique (jugador_id, club_id, torneo_id),
  check (
    fecha_hasta is null
    or fecha_desde is null
    or fecha_hasta >= fecha_desde
  )
);

